x = 10
y = 5

# Addition
print(x + y)  # Output: 15

# Subtraction
print(x - y)  # Output: 5

# Multiplication
print(x * y)  # Output: 50

# Division
print(x / y)  # Output: 2.0

# Integer division
print(x // y)  # Output: 2

# Modulus
print(x % y)  # Output: 0

# Exponentiation
print(x ** y)  # Output: 100000
